"""Set of constants for use by neutron tools """


def get_e():
    """charge on electron in coulombs"""
    return 1.60217662e-19


def get_p_mass():
    """mass of proton in g"""
    return 1.672623e-24


def get_n_mass():
    """mass of neutron in g"""
    return 1.674929e-24


def get_e_mass():
    """ mass of electron in g"""
    return 9.10938e-28
